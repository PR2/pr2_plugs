import roslib; roslib.load_manifest('pr2_plugs_executive')
import rospy
from pr2_msgs.msg import PowerState

def main():
    rospy.init_node('continuous_ops_battery_stub')
    pub = rospy.Publisher('power_state', PowerState)
    msg = PowerState()
    while not rospy.is_shutdown():
        msg.relative_capacity = int(raw_input('Enter battery level: '))
        pub.publish(msg)


if __name__ == '__main__':
    main()
